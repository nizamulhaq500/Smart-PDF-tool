// Enhanced loading spinner functionality
function showLoadingSpinner(button, text = 'Processing...') {
    if (button) {
        button.innerHTML = `<i class="fas fa-spinner fa-spin"></i> ${text}`;
        button.disabled = true;
    }
}

function hideLoadingSpinner(button, originalText, icon = 'fas fa-check') {
    if (button) {
        button.innerHTML = `<i class="${icon}"></i> ${originalText}`;
        button.disabled = false;
    }
}

// Enhanced file upload animations
